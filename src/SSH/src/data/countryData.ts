// Helper function to get country by code
export const getCountryByCode = (code: string): CountryData | undefined => {
  return countryData.find((country) => country.code === code)
}

// Helper function to validate postal code
export const validatePostalCode = (code: string, countryCode: string): boolean => {
  const country = getCountryByCode(countryCode)
  if (!country || !country.postalCodePattern) return true // If no pattern, accept any
  return country.postalCodePattern.test(code)
}

// Helper function to get states for a country
export const getStatesForCountry = (countryCode: string) => {
  const country = getCountryByCode(countryCode)
  return country?.states || []
}
